package net.imagej.ops.special;

/**
 * Interface for objects with two typed input parameters.
 * 
 * @author Curtis Rueden
 * @see UnaryInput
 * @see Output
 */
public interface BinaryInput<I1, I2> extends UnaryInput<I1> {

	I1 in1();
	I2 in2();

	void setInput1(I1 input1);
	void setInput2(I2 input2);

	// -- UnaryInput methods --

	@Override
	default I1 in() {
		return in1();
	}

	@Override
	default void setInput(final I1 input) {
		setInput1(input);
	}

}
